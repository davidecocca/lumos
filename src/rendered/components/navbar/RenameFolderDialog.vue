<template>
    <v-dialog
        :model-value="modelValue"
        @update:model-value="$emit('update:modelValue', $event)"
        max-width="520"
    >
        <v-card rounded="xl" elevation="8">
            <v-card-title class="d-flex align-center pt-5 pb-1 px-6">
                <v-avatar color="blue-lighten-5" size="36" class="mr-3">
                    <v-icon size="22" color="blue-darken-2">mdi-folder-edit</v-icon>
                </v-avatar>
                <div>
                    <div class="text-h6">Rename folder</div>
                    <div class="text-subtitle-2 text-medium-emphasis">Enter a new name for the folder.</div>
                </div>
            </v-card-title>

            <v-card-text class="px-6 pb-4">
                <v-text-field
                    v-model="folderName"
                    label="Folder name"
                    clearable
                    variant="outlined"
                    @click:clear="handleClear"
                    @keydown.enter="handleEnter"
                />
            </v-card-text>

            <v-divider />
            <v-card-actions class="px-6 py-3">
                <v-spacer />
                <v-btn variant="text" @click="closeDialog()">Close</v-btn>
                <v-btn color="primary" variant="tonal" @click="renameFolder" :disabled="!folderName.trim()">Save</v-btn>
            </v-card-actions>
        </v-card>
    </v-dialog>
</template>

<script setup>
import { ref } from 'vue'

// TODO: Set the initial value of folderName to the old folder name
const folderName = ref('')

const props = defineProps({
    modelValue: {
        type: Boolean,
        default: false
    },
    folderId: {
        type: Number,
        mandatory: true
    },
    oldfolderName: {
        type: String,
        mandatory: true
    }
})

const emit = defineEmits(['update:modelValue', 'rename-folder'])

const closeDialog = () => {
    emit('update:modelValue', false)
}

const handleClear = () => {
    folderName.value = ''
}

const renameFolder = () => {
    if (folderName.value.trim()) {
        emit('rename-folder', props.folderId, folderName.value.trim())
    }
}

const handleEnter = () => {
    if (folderName.value.trim()) {
        renameFolder()
    }
}
</script>